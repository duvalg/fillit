#include "../includes/libft.h"

size_t		ft_intlen(size_t const n)
{
	size_t	len;
	size_t	tmp;

	len = 1;
	tmp = n;
	while (tmp >= 10)
	{
		tmp /= 10;
		len++;
	}
	return (len);
}
